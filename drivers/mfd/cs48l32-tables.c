static const struct regmap_config cs48l32_spi_regmap = {
	.name = "cs48l32",
	.reg_bits = 32,
	.reg_stride = 4,
	.pad_bits = 32,
	.val_bits = 32,
	.reg_format_endian = REGMAP_ENDIAN_BIG,
	.val_format_endian = REGMAP_ENDIAN_BIG,

	.max_register = TACNA_IRQ1_EDGE_11,
	.readable_reg = cs48l32_readable_register,
	.volatile_reg = cs48l32_volatile_register,

	.cache_type = REGCACHE_RBTREE,
	.reg_defaults = cs48l32_reg_default,
	.num_reg_defaults = ARRAY_SIZE(cs48l32_reg_default),
};

static const struct regmap_config cs48l32_spi_dsp1_regmap = {
	.name = "cs48l32-dsp1",
	.reg_bits = 32,
	.reg_stride = 4,
	.pad_bits = 32,
	.val_bits = 32,
	.reg_format_endian = REGMAP_ENDIAN_BIG,
	.val_format_endian = REGMAP_ENDIAN_BIG,

	.max_register = 0x3845fe8,
	.rd_table = &cs48l32_dsp1_readable,
	.precious_table = &cs48l32_dsp1_precious,

	/* we don't currently need to cache any DSP1 registers */
	.cache_type = REGCACHE_NONE,
};

int cs48l32_init_spi_regmap(struct spi_device *spi, struct tacna *tacna)
{	/* notice if using caching without marking the registers volatile */
	BUILD_BUG_ON(cs48l32_spi_dsp1_regmap.cache_type != REGCACHE_NONE &&
		     cs48l32_spi_dsp1_regmap.volatile_table == NULL);

	tacna->regmap = devm_regmap_init_spi(spi, &cs48l32_spi_regmap);
	if (IS_ERR(tacna->regmap))
		return PTR_ERR(tacna->regmap);

	tacna->dsp_regmap[0] =
		devm_regmap_init_spi(spi, &cs48l32_spi_dsp1_regmap);
	if (IS_ERR(tacna->dsp_regmap[0]))
		return PTR_ERR(tacna->dsp_regmap[0]);

	return 0;
}
EXPORT_SYMBOL_GPL(cs48l32_init_spi_regmap);
